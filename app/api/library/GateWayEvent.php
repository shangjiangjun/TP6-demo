namespace app\api\library;

use GatewayWorker\Lib\Gateway;
use http\Client;
use think\facade\Log;
use Workerman\Worker;
use think\worker\Application;
// 特别注意：下方用到的Application属于think\work命名空间下的类，由于命名空间的更换，需要这样引入才能使用，否则会报错找不到这个类

/**
 * Worker 命令行服务类
 */
class GateWayEvent
{
    /**
     * onWorkerStart 事件回调
     * 当businessWorker进程启动时触发。每个进程生命周期内都只会触发一次
     *
     * @access public
     * @param  \Workerman\Worker    $businessWorker
     * @return void
     */
    public static function onWorkerStart(Worker $businessWorker)
    {
        $app = new Application;
        $app->initialize();
    }

    /**
     * onConnect 事件回调
     * 当客户端连接上gateway进程时(TCP三次握手完毕时)触发
     *
     * @access public
     * @param  int       $client_id
     * @return void
     */
    public static function onConnect($client_id)
    {
        Gateway::sendToCurrentClient("Your client_id is $client_id");
    }

    /**
     * onWebSocketConnect 事件回调
     * 当客户端连接上gateway完成websocket握手时触发
     *
     * @param  integer  $client_id 断开连接的客户端client_id
     * @param  mixed    $data
     * @return void
     */
    public static function onWebSocketConnect($client_id, $data)
    {
        // var_export($data);
        // Gateway::sendToCurrentClient("Your client_id is $client_id , " . json_encode($data));
        // Gateway::sendToCurrentClient(successMessage('connect'), true);
        $accessToken = $data['get']['token'] ?? '';
        Gateway::sendToCurrentClient("token:" . $accessToken);
        Gateway::sendToCurrentClient("是否在线:" . Gateway::isOnline($client_id));
        // 根据token 返回用户信息
        $client = new Client();

    }

    /**
     * onMessage 事件回调
     * 当客户端发来数据(Gateway进程收到数据)后触发
     *
     * @access public
     * @param  int       $client_id
     * @param  mixed     $data
     * @return void
     */
    public static function onMessage($client_id, $data)
    {
        Gateway::sendToAll($data);
    }

    /**
     * onClose 事件回调 当用户断开连接时触发的方法
     *
     * @param  integer $client_id 断开连接的客户端client_id
     * @return void
     */
    public static function onClose($client_id)
    {
        GateWay::sendToAll("client[$client_id] logout\n");
    }

    /**
     * onWorkerStop 事件回调
     * 当businessWorker进程退出时触发。每个进程生命周期内都只会触发一次。
     *
     * @param  \Workerman\Worker    $businessWorker
     * @return void
     */
    public static function onWorkerStop(Worker $businessWorker)
    {
        echo "WorkerStop\n";
    }
}
